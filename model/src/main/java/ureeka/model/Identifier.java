package ureeka.model;

import java.io.Serializable;

/**
 * Represents the unique identifier for an Entity
 */
public class Identifier implements Serializable {

	private static final long serialVersionUID = -7803979103299242148L;

	private final String identifier;

	public Identifier(String identifier) {
		super();
		if (identifier == null || "".equals(identifier.trim()))
			throw new IllegalArgumentException("Passed identifier was null or empty");
		this.identifier = identifier;
	}
	
	public String getIdentifier() {
		return identifier;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((identifier == null) ? 0 : identifier.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Identifier other = (Identifier) obj;
		if (identifier == null) {
			if (other.identifier != null)
				return false;
		} else if (!identifier.equals(other.identifier))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return identifier;
	}
	
}
